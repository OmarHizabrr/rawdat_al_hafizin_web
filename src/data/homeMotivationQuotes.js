function hashYmd(ymd) {
  let h = 0;
  for (let i = 0; i < ymd.length; i += 1) {
    h = Math.imul(31, h) + ymd.charCodeAt(i);
  }
  return Math.abs(h);
}

export function pickHomeMotivationQuote(todayYmd) {
  if (!todayYmd || typeof todayYmd !== "string") return QUOTES[0];
  const i = hashYmd(todayYmd) % QUOTES.length;
  return QUOTES[i];
}
