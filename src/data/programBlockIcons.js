const ICON_MAP = Object.fromEntries(ICON_ENTRIES.map((e) => [e.value, e.Icon]))

/** خيارات القائمة مع أيقونة لكل عنصر */
export const PROGRAM_BLOCK_ICON_OPTIONS = ICON_ENTRIES.map(({ value, label, group, Icon }) => ({
  value,
  label,
  group,
  Icon,
  detail: group,
  searchText: `${label} ${group} ${value}`,
}))

export function resolveProgramBlockIcon(name) {
  const key = String(name || '').trim()
  return ICON_MAP[key] || Circle
}

export function findProgramBlockIconOption(value) {
  return PROGRAM_BLOCK_ICON_OPTIONS.find((o) => o.value === value) ?? null
}
